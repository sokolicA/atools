#' Convenience function used to update the general parameters data
#'
#' @return List of parameters.
#' @export
#'
#' @examples
#' update_pars()
update_pars <- function() {
  pars <- list(
    palette_cb = c("#E69F00", "#56B4E9", "#CC79A7", "#009E73", "#F0E442", "#0072B2", "#D55E00",  "#999999")
  )
  pars
}
